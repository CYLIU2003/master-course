#!/usr/bin/env python3
from __future__ import annotations

import pathlib
import sys
import time

from fastapi.testclient import TestClient

sys.path.insert(0, str(pathlib.Path(__file__).resolve().parents[1]))

from bff.main import app


ENDPOINTS = [
    ("GET", "/api/app-state", None),
    ("GET", "/api/app/datasets", None),
    ("GET", "/api/scenarios", None),
]

BASELINE_DOC = pathlib.Path("docs/notes/performance_baseline.md")
RUNS = 5


def measure(method: str, path: str) -> dict:
    times: list[float] = []
    sizes: list[int] = []
    client = TestClient(app)
    for _ in range(RUNS):
        started = time.perf_counter()
        try:
            response = client.request(method, path)
            body = response.content
            elapsed_ms = (time.perf_counter() - started) * 1000.0
            times.append(elapsed_ms)
            sizes.append(len(body))
        except Exception as exc:
            return {"error": str(exc), "path": path, "method": method}
    sorted_times = sorted(times)
    return {
        "method": method,
        "path": path,
        "p50_ms": round(sorted_times[len(sorted_times) // 2], 1),
        "p95_ms": round(sorted_times[min(len(sorted_times) - 1, int(len(sorted_times) * 0.95))], 1),
        "avg_bytes": round(sum(sizes) / len(sizes)),
        "runs": RUNS,
    }


def main() -> None:
    results = []
    for method, path, _ in ENDPOINTS:
        print(f"Measuring {method} {path} ...", end=" ", flush=True)
        result = measure(method, path)
        results.append(result)
        if "error" in result:
            print(f"ERROR: {result['error']}")
        else:
            print(f"{result['p50_ms']}ms p50 / {result['avg_bytes']}B avg")

    lines = [
        "# Performance Baseline",
        "",
        "Generated by `tools/benchmark_api.py`",
        "",
        "## Results",
        "",
        "| Endpoint | p50 ms | p95 ms | avg bytes |",
        "|----------|--------|--------|-----------|",
    ]
    for result in results:
        if "error" in result:
            lines.append(f"| {result.get('path', '?')} | ERROR | - | - |")
        else:
            lines.append(
                f"| `{result['method']} {result['path']}` | {result['p50_ms']} | {result['p95_ms']} | {result['avg_bytes']} |"
            )
    successful = [result for result in results if "error" not in result]
    exceeding = [
        result
        for result in successful
        if (result["path"] == "/api/app-state" and result["p50_ms"] >= 200)
        or (result["path"] != "/api/app-state" and result["p50_ms"] >= 500)
        or result["avg_bytes"] >= 300_000
    ]
    heaviest = sorted(successful, key=lambda item: item["p50_ms"], reverse=True)[:3]
    largest = sorted(successful, key=lambda item: item["avg_bytes"], reverse=True)[:3]
    lines += [
        "",
        "## Performance Budget",
        "",
        "| Metric | Budget |",
        "|--------|--------|",
        "| `/api/app-state` | < 200ms p50 |",
        "| List/summary APIs | < 500ms p50 |",
        "| Summary payload | < 300 KB |",
        "| Run page readiness | < 2s |",
        "",
        "## Endpoints Exceeding Budget",
        "",
    ]
    if exceeding:
        lines += [f"- `{item['method']} {item['path']}`" for item in exceeding]
    else:
        lines.append("- None in the current baseline sample.")
    lines += [
        "",
        "## Top 3 Heaviest Endpoints",
        "",
    ]
    lines += [
        f"{idx}. `{item['method']} {item['path']}` - {item['p50_ms']}ms p50"
        for idx, item in enumerate(heaviest, start=1)
    ] or ["1. No successful measurements"]
    lines += [
        "",
        "## Top 3 Largest Payloads",
        "",
    ]
    lines += [
        f"{idx}. `{item['method']} {item['path']}` - {item['avg_bytes']} bytes avg"
        for idx, item in enumerate(largest, start=1)
    ] or ["1. No successful measurements"]
    BASELINE_DOC.parent.mkdir(parents=True, exist_ok=True)
    BASELINE_DOC.write_text("\n".join(lines), encoding="utf-8")
    print(f"\nBaseline written: {BASELINE_DOC}")


if __name__ == "__main__":
    main()
